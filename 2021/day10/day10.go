package main

import (
	"flag"
	"fmt"
	"log"
	"sort"
	"strings"
	"time"
)

func main() {
	start := time.Now()
	log.Printf("Starting...")

	testInputPtr := flag.Bool("testInput", false, "a bool")
	flag.Parse()

	if *testInputPtr {
		fmt.Println("Using test Input")
		input = inputTest
	}

	lines := parseInput(input)

	charOpeners := map[string]string{"(": ")", "[": "]", "{": "}", "<": ">"}
	score, stackScores := 0, []int{}

	for _, line := range lines {
		stack, isCorrupt := []string{}, false

		for _, char := range strings.Split(line, "") {
			if charCloser, ok := charOpeners[char]; ok {
				stack = append(stack, charCloser)
				continue
			}

			lastPair := stack[len(stack)-1]
			stack = stack[:len(stack)-1]
			if lastPair != char {
				isCorrupt = true
				score += calcScore(char)
			}
		}

		if !isCorrupt {
			stackScores = append(stackScores, calcStackScore(stack))
		}
	}
	sort.Ints(stackScores)

	fmt.Println("Part 1:", score)
	fmt.Println("Part 2:", stackScores[len(stackScores)/2])

	log.Printf("Duration: %s", time.Since(start))
}

func calcStackScore(s []string) int {
	scores := map[string]int{")": 1, "]": 2, "}": 3, ">": 4}

	score := 0

	for i := len(s) - 1; i >= 0; i-- {
		score = (score * 5) + scores[s[i]]
	}

	return score
}

func calcScore(c string) int {
	scores := map[string]int{")": 3, "]": 57, "}": 1197, ">": 25137}

	return scores[c]
}

func parseInput(input string) []string {
	return strings.Split(input, "\n")
}

